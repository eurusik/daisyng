/**
 * Type representing the possible sizes for a button.
 *
 * This type allows for setting one of the following size values for a button:
 *
 * - `xs`: Extra small size.
 * - `sm`: Small size.
 * - `md`: Medium size (default).
 * - `lg`: Large size.
 */
export type ButtonSize = 'xs' | 'sm' | 'md' | 'lg';
